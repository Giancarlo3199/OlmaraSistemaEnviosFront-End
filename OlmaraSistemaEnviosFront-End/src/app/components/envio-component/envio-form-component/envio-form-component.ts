  bloquearFormularioParaEmpleado(): void {

  // Bloquear todo el formulario al momento de editar envio para el empleado
  this.form.disable({ emitEvent: false });

  // Habilitar SOLO la fecha de recepcion y fecha de transito para el empleado
  this.form.get('fechaRecepcion')?.enable({ emitEvent: false });
  this.form.get('fechaTransito')?.enable({ emitEvent: false });

}

 guardar(): void {
  if (this.form.invalid) {
    this.form.markAllAsTouched();
    return;
  }

const rawValue = this.form.getRawValue();

const payload = {
  ...rawValue,
  ...rawValue.datosInternos,
  ...rawValue.datosExternos,
};

delete payload.datosInternos;
delete payload.datosExternos;

  if (this.id) {
    this.envioService.modificar(payload).subscribe(() => {
      this.router.navigate(['/envios']);
    });
  } else {
    this.envioService.registrar(payload).subscribe(() => {
      this.router.navigate(['/envios']);
    });
  }
}


